# 6. YAPAY SİNİR AĞLARI (YSA / ANN) MODELLEMESİ (Dropout & Early Stopping)

from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import Dense, Dropout
from tensorflow.keras.callbacks import EarlyStopping
from sklearn.metrics import confusion_matrix, classification_report, roc_curve, roc_auc_score
from sklearn.model_selection import train_test_split

print("YAPAY SİNİR AĞI (YSA) MODELİ EĞİTİLİYOR\n")

"""
Metodolojik Savunma: Yapay Sinir Ağları (TensorFlow/Keras) mimarisi gereği Scikit-Learn veya
Imblearn Pipeline'ları ile doğrudan (ve stabil) uyumlu çalışmaz. Bu nedenle YSA eğitiminde SMOTE
işlemi hücre içinde statik olarak uygulanmış, ardından test setine kesinlikle dokunulmadan kendi
içinde bir doğrulama (validation) setine ayrılmıştır. Sızıntı (Leakage) söz konusu değildir.
"""

from sklearn.model_selection import train_test_split
from imblearn.over_sampling import SMOTE

# ADIM 1: Veriyi ÖNCE bölüyoruz (Sızıntıyı önlemek için)
X_train_sub, X_val_nn, y_train_sub, y_val_nn = train_test_split(
    X_train_scaled, y_train, test_size=0.2, random_state=42)

# SMOTE işlemini SADECE ayırdığımız eğitim alt-kümesine uyguluyoruz
# Böylece Validation (Doğrulama) setine sentetik veri sızmamış oluyor.
smote_nn = SMOTE(random_state=42)
X_train_nn, y_train_nn = smote_nn.fit_resample(X_train_sub, y_train_sub)

# ADIM 2: MODEL MİMARİSİ
ysa_model = Sequential()
ysa_model.add(Dense(units=32, activation='relu', input_shape=(X_train_nn.shape[1],)))
ysa_model.add(Dropout(0.3)) #Nöronların %30'u kapatılarak ezberleme engellenir
ysa_model.add(Dense(units=16, activation='relu'))
ysa_model.add(Dropout(0.3))
ysa_model.add(Dense(units=1, activation='sigmoid'))

ysa_model.compile(optimizer='adam', loss='binary_crossentropy', metrics=['accuracy'])

# ADIM 3: ERKEN DURDURMA (Early Stopping)
early_stop = EarlyStopping(monitor='val_loss', patience=5, restore_best_weights=True)

# ADIM 4: EĞİTİM SÜRECİ
history = ysa_model.fit(
    X_train_nn, y_train_nn,
    epochs=50,
    batch_size=32,
    validation_data=(X_val_nn, y_val_nn),
    verbose=1,
    callbacks=[early_stop]
)

# ADIM 5: TAHMİNLER VE SINIFLANDIRMA RAPORU
y_pred_probs = ysa_model.predict(X_test_scaled, verbose=0).flatten()
y_pred = (y_pred_probs > 0.5).astype(int) # 0.5 eşiğine göre 1 veya 0 yapıyoruz

print("\nYSA MODELİ SINIFLANDIRMA RAPORU:")
print(classification_report(y_test, y_pred))

# ADIM 6: GÖRSELLEŞTİRME
sns.set_theme(style="whitegrid")
fig, axes = plt.subplots(2, 2, figsize=(16, 12))

best_epoch = np.argmin(history.history['val_loss'])

