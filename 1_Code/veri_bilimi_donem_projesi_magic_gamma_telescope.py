# ADIM 2: HİPERPARAMETRE OPTİMİZASYONU (Pipeline İçinde SMOTE ile)
# Parametre isimlerinin başına algoritmanın ismini (rf__) ekliyoruz ki Pipeline tanısın
rf_param_grid = {
    'rf__n_estimators': [50, 100, 200],
    'rf__max_depth': [None, 10, 20, 30],
    'rf__min_samples_split': [2, 5, 10],
    'rf__criterion': ['gini', 'entropy']
}

# Veri sızıntısını engellemek için SMOTE işlemini Pipeline içine alıyoruz
smote_pipeline = ImbPipeline([
    ('smote', SMOTE(random_state=42)),
    ('rf', RandomForestClassifier(random_state=42))
])

rf_random = RandomizedSearchCV(estimator=smote_pipeline,
                               param_distributions=rf_param_grid,
                               n_iter=10, cv=3, verbose=0, random_state=42)

#  SMOTE UYGULANMIŞ VERİYİ DEĞİL, ORİJİNAL VERİYİ VERİYORUZ (SMOTE'u içeride kendi yapacak)
