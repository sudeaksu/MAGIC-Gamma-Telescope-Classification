modeller = {
    "Lojistik Regresyon": LogisticRegression(),
    f"k-En Yakın Komşu (k={best_k})": KNeighborsClassifier(n_neighbors=best_k),
    "Karar Ağacı (Gini)": DecisionTreeClassifier(criterion='gini', random_state=42),
    "Karar Ağacı (Entropi)": DecisionTreeClassifier(criterion='entropy', random_state=42),
    "Rastgele Orman": RandomForestClassifier(n_estimators=100, random_state=42),
    "Basit Bayes (Naive Bayes)": GaussianNB(),
}

cv = StratifiedKFold(n_splits=5, shuffle=True, random_state=42)

# Kod Kalitesi: İşlemleri tekrar kullanılabilir bir fonksiyon içine alıyoruz
def modelleri_karsilastir_ve_raporla(modeller_dict, X_train, y_train, X_test, y_test, cv_kfold):
    print("Algoritmalar eğitiliyor ve Çapraz Doğrulama (CV) uygulanıyor...")
    sonuclar = []
    auc_sonuclar = []

    for isim, model in modeller_dict.items():
        # Test Skoru Hesaplamaları
        model.fit(X_train, y_train)
        y_pred = model.predict(X_test)

        # Çapraz Doğrulama (CV) Skoru Hesaplama
        cv_skorlari = cross_val_score(model, X_train, y_train, cv=cv_kfold, scoring='accuracy')

        sonuclar.append({
            "Algoritma": isim,
            "CV Doğruluk (Ort)": round(cv_skorlari.mean(), 4),
            "CV Std. Sapma": round(cv_skorlari.std(), 4),
            "Test Doğruluk": round(accuracy_score(y_test, y_pred), 4),
            "Hassasiyet": round(precision_score(y_test, y_pred), 4),
            "Duyarlılık": round(recall_score(y_test, y_pred), 4),
            "F1-Skoru": round(f1_score(y_test, y_pred), 4)
        })

        # ROC-AUC Olasılık Hesaplaması
        if hasattr(model, "predict_proba"):
            probs = model.predict_proba(X_test)[:, 1]
            auc_sonuclar.append({
                "Algoritma": isim,
                "ROC-AUC Skoru": round(roc_auc_score(y_test, probs), 4)
            })

    # DataFrameleri oluştur ve döndür
    df_sonuc = pd.DataFrame(sonuclar).sort_values(by="Test Doğruluk", ascending=False).reset_index(drop=True)
    df_auc = pd.DataFrame(auc_sonuclar).sort_values("ROC-AUC Skoru", ascending=False).reset_index(drop=True)
    return df_sonuc, df_auc

# Fonksiyonu Çağırıyoruz
sonuclar_df, auc_df = modelleri_karsilastir_ve_raporla(modeller, X_train_scaled, y_train, X_test_scaled, y_test, cv)

