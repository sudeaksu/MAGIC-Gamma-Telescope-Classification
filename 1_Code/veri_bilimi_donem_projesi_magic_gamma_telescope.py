axes[0, 0].plot(history.history['accuracy'], label='Eğitim Doğruluğu', color='#1f77b4', linewidth=2)
axes[0, 0].plot(history.history['val_accuracy'], label='Doğrulama (Validation)', color='#ff7f0e', linewidth=2)
axes[0, 0].axvline(x=best_epoch, color='red', linestyle='--', label=f'Optimum Nokta (Epoch {best_epoch+1})')
axes[0, 0].set_title('YSA Model Doğruluğu (Accuracy)', fontsize=13)
