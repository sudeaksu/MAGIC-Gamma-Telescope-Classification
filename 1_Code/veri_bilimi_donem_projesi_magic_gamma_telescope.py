# ADIM 1: VARSAYILAN (DEFAULT) MODELİN EĞİTİLMESİ (Adil kıyaslama için Pipeline ile)
rf_default_pipe = ImbPipeline([
    ('smote', SMOTE(random_state=42)),
    ('rf', RandomForestClassifier(random_state=42))
])
rf_default_pipe.fit(X_train_scaled, y_train)

# Skorları da sızıntısız orijinal veri üzerinden kontrol ediyoruz
default_train_score = rf_default_pipe.score(X_train_scaled, y_train)
